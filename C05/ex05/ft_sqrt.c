#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 0)
		return (0);
	i = 1;
	while (i * i != nb)
	{
		if (i > nb)
			return (0);
		i++;
	}
	return (i);
}
